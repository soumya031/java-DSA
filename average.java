public class average {
    public static void main(String[] args){
        int a = 5;
        int b = 7;
        int c = 9;
        int d = 11;
        int e = 13;
        int sum = a + b + c + d + e;
        int average = sum / 5;
        System.out.println("The average of 5, 7, 9, 11, and 13 is " + average);
    }
}
